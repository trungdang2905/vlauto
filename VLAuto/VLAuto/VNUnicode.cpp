void TCVN3ToUnicode(LPCSTR szTCVN3String, LPWSTR szUnicodeString, int nMaxLen)
{
	int i, j;
	for (i = 0; szTCVN3String[i] && i < nMaxLen; i++)
	{
		szUnicodeString[i] = LOBYTE(szTCVN3String[i]);
	}
	szUnicodeString[i] = 0;
	for (i = 0; szTCVN3String[i] && i < nMaxLen; i++)
	{
		for (j = 0; j < CHAR_ARRAY_SIZE; j++)
			if (szTCVN3String[i] == (char)TCVN3_CODE[j])
			{
				szUnicodeString[i] = UNICODE_CODE[j];
				break;
			}
		if (j == CHAR_ARRAY_SIZE && szTCVN3String[i] < 0)
			szUnicodeString[i] = 0x0020;
	}
}

void UnicodeToTCVN3(LPCWSTR szUnicodeString, LPSTR szTCVN3String, int nMaxLen)
{
	int i, j;
	for (i = 0; szUnicodeString[i] && i < nMaxLen; i++)
	{
		szTCVN3String[i] = LOBYTE(szUnicodeString[i]);
	}
	szTCVN3String[i] = 0;
	for (i = 0; szUnicodeString[i] && i < nMaxLen; i++)
	{
		for (j = 0; j < CHAR_ARRAY_SIZE; j++)
			if (szUnicodeString[i] == (wchar_t)UNICODE_CODE[j])
			{
				szTCVN3String[i] = TCVN3_CODE[j];
				break;
			}
	}
}
